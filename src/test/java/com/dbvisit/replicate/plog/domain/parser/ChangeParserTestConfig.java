    protected List<EntryRecord> parseEntryRecord (byte [] data) 
    throws Exception {
        InputStream is = new ByteArrayInputStream (data);
        DataInputStream dis = new DataInputStream (is);
        
        List<EntryRecord> records = new ArrayList<EntryRecord>();

        int numBytes = data.length;
        int numRead = 0;
        
        while (numRead < numBytes) {
            EntryRecord rec = EntryRecordParser.getParser().parse(dis);
            parseEntryRecordOwner (rec);
            records.add(rec);
            numRead += rec.getSize();
        }
        
        dis.close();
        
        return records;
    }
    
    protected void parseEntryRecordOwner (EntryRecord rec)
    throws Exception {
        String schema = null;
        String objectOwner = null;
        String objectName  = null;
        
        Map<EntryTagType, List<EntryTagRecord>> tags = rec.getEntryTags();
        EntryTagType type  = EntryTagType.TAG_OBJ_OWNER;
        EntryTagRecord tag = null;

        if (tags.containsKey(type)) {
            tag = tags.get (type).get(0);
            objectOwner = 
                SimpleDataDecoder.decodeCharString(tag.getRawData());
        }

        type = EntryTagType.TAG_OBJ_NAME;
        if (tags.containsKey(type)) {
            tag = tags.get (type).get(0);
            objectName =
                SimpleDataDecoder.decodeCharString(tag.getRawData());
        }
    
        if (objectOwner == null || objectName == null) {
            /* nothing encoded, try and lookup information in dictionary
             * cache, if present
             */
            type = EntryTagType.TAG_OBJ_ID;
            if (tags.containsKey(type)) {
                tag = tags.get (type).get(0);
                int objectId = 
                    SimpleDataDecoder.decodeInteger(tag.getRawData());

                if (rec.hasColumnData()) {
                    /* try cache */
                    if (plog.getDictionary().containsKey (objectId)) {
                        objectOwner =
                            plog.getDictionary().get (objectId).getOwner();

                        objectName =
                            plog.getDictionary().get (objectId).getName();
                    }
                }
            }
        }
        
        if (objectOwner != null && objectName != null) {
            schema = 
                objectOwner.replace("$", "") + "." + 
                objectName.replace("$", "");
        }
        
        rec.setOwner(schema);
    }
    
