    protected void testPLOGTableOffsets () throws Exception {
        InputStream is = new ByteArrayInputStream (dataByteArray);
        DataInputStream dis = new DataInputStream (is);
        
        PlogFile p = new PlogFile();
        
        PlogStreamReader psr = new PlogStreamReader();
        p.setReader(psr);
        
        DomainReader r = psr.getDomainReader();
        
        r.setDomainParsers(domainParsers);

        psr.setPlog(p);
        psr.setPlogStream(dis);
        r.setPersistCriteria(persistCriteria);
        r.enableFlushLastTransactions();
        psr.prepare();
        
        assertTrue (
            "Expecting valid PLOG stream",
            p.isValid()
        );
        
        assertTrue (
            "Expecting stream offset > " + PlogFile.PLOG_DATA_BYTE_OFFSET +
            ", got: " + psr.getOffset(),
            psr.getOffset() >= PlogFile.PLOG_DATA_BYTE_OFFSET
        );
        
        /* flush every record */
        psr.setFlushSize(1);
        
        ReplicateOffset prevOffset = new ReplicateOffset (0L, 0L);
        while (!psr.isDone()) {
            psr.read();
            
            if (psr.canFlush()) {
                DomainRecord dr = psr.flush().get(0);
            
                logger.info (
                    dr.getRecordType() + ": " +
                    dr.getRecordSchema() + " " +
                    dr.getRecordSCN() + " " +
                    (dr.getRecordOffset().getPlogOffset() 
                         - dr.getRawRecordSize())
                );
                
                if (dr.isChangeRecord()) {
                    assertTrue (
                        "Check file offsets, previous offset:" + 
                        prevOffset + " current offset: " + 
                        dr.getRecordOffset() + " record size: " + 
                        dr.getRawRecordSize(),
                        prevOffset.getPlogUID() == 0L || (
                            prevOffset.compareTo(dr.getRecordOffset()) < 0 &&
                            prevOffset.getPlogUID() 
                                == dr.getRecordOffset().getPlogUID() &&
                            prevOffset.getPlogOffset() 
                                == dr.getRecordOffset().getPlogOffset() 
                                   - dr.getRawRecordSize()
                        )
                    );
                    prevOffset = dr.getRecordOffset();
                }
            }
        }
        
        assertTrue (
            "Footer has been read, reader must be done",
            psr.isDone()
        );
        
        p.close();
        
    }
    
