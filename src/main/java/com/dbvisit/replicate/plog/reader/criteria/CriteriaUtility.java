/**
 * Utility class for criteria
 */
public class CriteriaUtility {
    /**
     * Utility function for converting a wildcard string for Criteria
     * to internal Java regular expression
     * 
     * @param wildcard search term containing wildcard to be converted to
     *                 compatible regular expression
     *
     * @return String containing regular expression for wildcard search
     */
    public static String wildcardToRegex(String wildcard){
        StringBuffer s = new StringBuffer(wildcard.length());
        s.append('^');
        for (int i = 0, is = wildcard.length(); i < is; i++) {
            char c = wildcard.charAt(i);
            switch(c) {
                case '*':
                    s.append(".*");
                    break;
                case '?':
                    s.append(".");
                    break;
                case '(': case ')': case '[': case ']': case '$':
                case '^': case '.': case '{': case '}': case '|':
                case '\\':
                    s.append("\\");
                    s.append(c);
                    break;
                default:
                    s.append(c);
                    break;
            }
        }
        s.append('$');

        return(s.toString());
    }

}
